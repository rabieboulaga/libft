#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	unsigned char	*src;
	unsigned char	s;
	size_t			i;

	src = (unsigned char *)str;
	s = (unsigned char)c;
	i = 0;
	while (i < n)
	{
		if (src[i] == s)
			return (&src[i]);
		i++;
	}
	return (NULL);
}
